def analyze_grid(grid: str):
    rows = [r for r in grid.split("\n") if r]
    if not rows:
        return {"valid": False}

    width = len(rows[0])
    if any(len(r) != width for r in rows):
        return {"valid": False}

    cells = list("".join(rows))
    unique = set(cells)

    return {
        "valid": True,
        "rows": len(rows),
        "cols": width,
        "unique": unique,
        "unique_count": len(unique),
        "cells": cells
    }

